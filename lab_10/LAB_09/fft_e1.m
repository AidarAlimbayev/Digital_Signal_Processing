function [MODm,m] = fft_e1(MODX,e1)
% ќпределение значений модул€ ƒѕ‘ и частот полезного сигнала 
%
% MODX Ц вектор значений модул€ ƒѕ‘ смеси сигнала с шумом 
% e1 Ц заданный порог
%
% MODm Ц вектор значений модул€ ƒѕ‘ полезного сигнала
% m Ц вектор значений частот полезного сигнала   
%
i = 1;
MAX = max(MODX);
for k = 1:length(MODX)
    if (MODX(k)/MAX)>e1
       MODm(i) = MODX(k);
       m(i) = k-1;	
       i = i+1;
    end
end






